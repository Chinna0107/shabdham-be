async function seedExtra() {
  try {
    let newsCount = 0;

    for (const cat of extraCategories) {
      let res = await pool.query(
        "INSERT INTO categories (name, slug, show_in_header) VALUES ($1, $2, $3) ON CONFLICT (slug) DO UPDATE SET name = $1 RETURNING id",
        [cat.name, cat.slug, true]
      );
      let catId = res.rows[0].id;
      
      let tpls = templates[cat.slug];
      
      let promises = [];
      for (let i = 0; i < 15; i++) {
        let tpl = tpls[i % tpls.length];
        let title = `[${cat.name}] ${tpl.title} - ${i+1}`;
        let slug = `${cat.slug}-news-${i+1}-${Date.now()}-${Math.floor(Math.random()*1000)}`;
        
        let contentHtml = `<p><strong>${cat.name}:</strong> ${tpl.excerpt}</p>`;
        // Add 10 lines of content
        longContentParagraphs.forEach(p => {
          contentHtml += `<p>${p}</p>`;
        });
        contentHtml += `<p><strong>మరిన్ని తాజా వార్తల కోసం అప్‌డేట్స్ చూస్తూ ఉండండి.</strong></p>`;

        let image = `https://picsum.photos/seed/${cat.slug}-${i}/800/480`;

        promises.push(
          pool.query(
            "INSERT INTO news (title, slug, content, excerpt, image, author, category_id, is_published, is_trending) VALUES ($1, $2, $3, $4, $5, $6, $7, $8, $9)",
            [title, slug, contentHtml, tpl.excerpt, image, 'Admin', catId, true, i < 3]
          )
        );
      }
      await Promise.all(promises);
      newsCount += 15;
    }

    console.log(`✅ Seeded ${extraCategories.length} extra categories (politics, entertainment, sports, crime).`);
    console.log(`✅ Inserted ${newsCount} extremely long news articles.`);
    process.exit(0);

  } catch (err) {
    console.error(err);
    process.exit(1);
  }
}

seedExtra();
